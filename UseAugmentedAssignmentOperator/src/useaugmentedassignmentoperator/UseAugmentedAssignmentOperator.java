
package useaugmentedassignmentoperator;

public class UseAugmentedAssignmentOperator {
    public static void main(String[] args) {
        int n1 = 10;

        System.out.println(n1+=8);
        System.out.println("n1+=8: "+ n1);
        System.out.println(n1-=8);
        System.out.println("n1-=8: "+ n1);
        System.out.println(n1*=8);
        System.out.println("n1*=8: "+ n1);
        System.out.println(n1+=8);
        System.out.println("n1/=8: "+ n1);
        System.out.println(n1%=8);
        System.out.println("n1%=8: "+ n1+ "/n");
        
        
        int i= 6;
        System.out.println("i= "+ i);
        System.out.println("i++= "+ i++);
        System.out.println("++i= "+ ++i);
        System.out.println("i--= "+ i--);
        System.out.println("--i= "+ --i);
       
         
    }
    
}
